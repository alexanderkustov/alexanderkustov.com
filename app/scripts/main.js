function recentWork(){
	"use strict";
    $('#main').hide('slow');
    $('#quote').hide('slow');
    $('#friends').hide('slow');
    $('#friends-logos').hide('slow');
    $('#projects').show('slow');
    $('#work').show('slow');

}

function resume(){
	"use strict";
	$('#quote').show('slow');
	$('#main').hide('slow');
	$('#projects').hide('slow');
    $('#work').hide('slow');
	$('#friends').hide('slow');
    $('#friends-logos').hide('slow');
}

